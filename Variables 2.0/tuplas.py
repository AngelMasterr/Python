# crear una tupla con tuple()
tupla_1 = tuple(["dato1", "dato2"])

# crear una tupla con ()
tupla_2 = ("dato1", "dato2")

# crear una tupla sin nada, solo comas
tupla_3 = "dato1", "dato2"

# crear una tupla de un solo dato, solo se pone coma la final del unico dato
tupla_4 = "dato1",

print(type(tupla_1))
print(type(tupla_2))
print(type(tupla_3))
print(type(tupla_4))